// ── Public entry point ──────────────────────────────────────────

/**
 * Dev Agent: scaffolds feature folder structure based on project type.
 * Supports Flutter (Dart) and C# (.NET) projects.
 * Does NOT overwrite existing files.
 */
export async function devAgent(
  architecture: Architecture,
  projectPath: string,
  featureName: string,
  codeAnalysis: CodeAnalysis
): Promise<string[]> {
  const projectType = codeAnalysis.projectType;
  logger.info(`Dev Agent: scaffolding "${featureName}" for ${projectType} project`);

  const createdFiles =
    projectType === "dotnet"
      ? await scaffoldDotnet(projectPath, featureName, architecture)
      : await scaffoldFlutter(projectPath, featureName, architecture);

  logger.info(`Dev Agent: scaffolding complete — ${createdFiles.length} files created`);
  return createdFiles;
}
